import argparse
import json
from pathlib import Path


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Convert a local evaluation summary into the JSON format expected by the web UI."
    )
    parser.add_argument(
        "--summary",
        type=str,
        required=True,
        help="Path to summary.json generated by src.test_inference",
    )
    parser.add_argument(
        "--output",
        type=str,
        required=True,
        help="Destination JSON path for the web app",
    )
    parser.add_argument(
        "--mode",
        type=str,
        default="quick",
        help="Mode label to expose in the exported web JSON",
    )
    parser.add_argument(
        "--max_samples",
        type=int,
        default=100,
        help="Max samples label to expose in the exported web JSON",
    )
    parser.add_argument(
        "--examples_key",
        type=str,
        default="best_examples",
        choices=["best_examples", "worst_examples"],
        help="Which example list from the summary to export",
    )
    parser.add_argument(
        "--n_examples",
        type=int,
        default=4,
        help="Number of examples to export to the web JSON",
    )
    args = parser.parse_args()

    summary_path = Path(args.summary)
    output_path = Path(args.output)
    summary = json.loads(summary_path.read_text(encoding="utf-8"))

    metrics = summary.get("metrics", {})
    split = summary.get("split", {})
    examples = summary.get(args.examples_key, [])[: max(0, int(args.n_examples))]

    exported = {
        "checkpoint": Path(summary.get("checkpoint", "")).name or str(summary_path),
        "mode": str(args.mode),
        "maxSamples": int(args.max_samples),
        "metrics": {
            "cer": float(metrics.get("cer", 0.0)),
            "wer": float(metrics.get("wer", 0.0)),
            "exactMatch": float(metrics.get("sequence_accuracy", 0.0)),
            "samples": int(split.get("val_cases_scored", 0)),
        },
        "examples": [
            {
                "gt": str(item.get("gt", "")),
                "pred": str(item.get("pred", "")),
            }
            for item in examples
        ],
    }

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(json.dumps(exported, indent=2), encoding="utf-8")
    print(json.dumps({"output": str(output_path)}, indent=2))


if __name__ == "__main__":
    main()
